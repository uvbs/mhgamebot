--脚本加载标志
已经加载战斗脚本 = false

--开始
function 脚本入口()
	
	延迟(1)

	if 已经加载战斗脚本 == false then
		调试信息("加载战斗脚本")
		dofile("任务-战斗.lua")
		已经加载战斗脚本 = true
	end

	检测需要人工()
	检测离线()
	关闭无关窗口()
	等待标识消失()
	处理死亡()




	状态 = 获取玩家状态()
	if 状态 == 战斗状态 then
		脚本_战斗结束 = true
		if 存在图片("战斗-菜单2", 0.8) then
			战斗回调()
		elseif 存在图片("宠物战斗菜单", 0.8) then
			宠物战斗回调()
		end

	elseif 状态 == 正常状态 then

		匹配到任务 = 匹配任务()
		if (匹配到任务 == false) then
			通用_主线()
		end

	elseif 状态 == 动画状态 then
		点击图片("跳过动画")
	else
		调试信息("未知状态..")
	end

end
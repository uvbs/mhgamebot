--脚本加载标志
已经加载战斗脚本 = false

--开始
function 脚本入口()

    local 匹配到任务 = true

	if 已经加载战斗脚本 == false then
		调试信息("加载战斗脚本")
		dofile("任务-战斗.lua")
		已经加载战斗脚本 = true
	end

	检测离线()
	关闭无关窗口()
	状态 = 获取玩家状态()
	if 状态 == 战斗状态 then
		调试信息("战斗状态")
		战斗回调()
	elseif 状态 == 正常状态 then
		匹配到任务 = 匹配任务()
	elseif 状态 == 未知状态 then
		调试信息("未知状态..")
	end

	if (匹配到任务 == false) then
		通用_主线()
	end

end
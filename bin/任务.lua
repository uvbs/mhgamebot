--没有匹配到任务的时候, 但是有可点击的下划线时调用这个函数
function 通用_主线()
	调试信息("默认的任务处理")
	
	等待标识消失()
	关闭无关窗口()
    点击下划线()



	if 存在图片("战斗", 0.5) then
		调试信息("通用任务处理中检测到战斗标志")
		x, y = 获得图片位置("战斗", 0.5)
		if x < 200 then
			移动鼠标到(x, y)
			确保接触到NPC()
			按键("ALT+A")
			点击()
		else
			--靠近到右面
			if 脚本_靠近左面 then
				点击图片("战斗", 50, 50)  --靠近
				脚本_靠近左面 = false
			else
				点击图片("战斗", -50, 50)  --靠近
				脚本_靠近左面 = true
			end

			等待停止奔跑()

			按键("F9")
			按键("ALT+H")

			x, y = 获得图片位置("战斗", 0.5)

			移动鼠标到(x, y+50)
			确保接触到NPC()
			点击()  --对话
			等待对话框出现()
		end

	
		if 存在图片("对话框/新手主线") then
			点击对话框("新手主线")
			点击对话框("对话框-战斗")
		end

		if 存在图片("对话框/对话框-战斗") then
			点击对话框("对话框-战斗")
		end

		return
	end

   	对话()

	if 存在图片("对话框/新手主线") then
		点击对话框("新手主线")
		等待对话框出现()
	end

	if 存在图片("对话框/对话框-战斗") then
		点击对话框("对话框-战斗")
	end

	if 存在图片("对话框/王夫人任务") then
		点击对话框("王夫人任务")
	end

end


end

function 找驿站老板()
	if(当前城市("长安城")) then
		点击小地图("地图-赌");
		隐藏玩家()
		if(存在图片("驿站老板", 5)) then 点击图片("驿站老板", 5) end
		if(存在图片("驿站老板1", 5)) then  点击图片("驿站老板1", 5)  end
		if(存在图片("跑镖-驿站老板2", 5)) then  点击图片("跑镖-驿站老板2", 5)  end
			if(存在图片("跑镖-驿站老板3", 5)) then  点击图片("跑镖-驿站老板3", 5)  end
	
		点击对话框内图片("对话框-是的我要去");
	end
end


function 跑镖结束给予(NPC名字)
	--走到旁边(NPC名字)
	隐藏玩家()
	local x, y = 移动鼠标到(NPC名字, 5)
	按键("ALT+G") --给予
	点击(x, y)
	右击("二级镖")
	点击图片("确定给予")
end

function 出领取跑镖门()
